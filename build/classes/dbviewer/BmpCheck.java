  public static void main(String[] args) {
    JPanel jp = new BmpCheck();
    JFrame jf = new Frame4(jp);
    jf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
    File file = new File(GlobalVariables.BMP);
    String[] files = file.list();
    DefaultListModel<String> lm = new DefaultListModel<>();
    for (int i = 0; i < files.length; i++) {
      if (files[i].endsWith(".gif")) {
        String path = GlobalVariables.BMP + FS + files[i];
//        System.out.println("" + " = " + path);
        lm.addElement(path);
      }
    }
    jList1.setModel((ListModel<String>) lm);
    jf.setBackground(Color.blue);
    jp.setBackground(Color.blue);
    jLabel1.setBackground(Color.blue);
    jList1.setSelectedIndex(0);
  }

