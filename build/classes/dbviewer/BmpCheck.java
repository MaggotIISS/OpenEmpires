  private void showPic() {
    super.paintComponent(jLabel1.getGraphics());
    File f = new File(jList1.getSelectedValue().toString());
    try {
      BufferedImage image = ImageIO.read(f);

      jLabel1.getGraphics().drawImage(image, 100, 100, null);
    } catch (IOException ex) {
      Logger.getLogger(BmpCheck.class.getName()).log(Level.SEVERE, null, ex);
    }
  }

