  private void jLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelMouseClicked
    unColor();
    int i = evt.toString().lastIndexOf("on ") + 3;
    String s = evt.toString().substring(i);
    i = Integer.parseInt(s) - 1;
    labs[i].setBackground(Color.white);
    if ((evt.getClickCount() > 1)) {
      if (Panel4WordGen.ok == true) {
        boolean ok = false;
        while (ok != true) {
          labs[i].setText(JOptionPane.showInputDialog("Syllable structure",
            labs[i].getText()).toUpperCase());
          String st = labs[i].getText().toUpperCase();
          switch (st) {
            case "":
            case "\t":
            case "C":
            case "V":
            case "CV":
            case "VC":
            case "CVC": {
              ok = true;
              break;
            }
            default: {
              labs[i].setText("");
              ok = false;
            }
          }
        }
      }
    }
  }//GEN-LAST:event_jLabelMouseClicked
